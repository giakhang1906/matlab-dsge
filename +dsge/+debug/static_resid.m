function [lhs, rhs] = static_resid(y, x, params)
T = NaN(11, 1);
lhs = NaN(19, 1);
rhs = NaN(19, 1);
T(1) = 1/params(4);
T(2) = (y(14)/y(13))^(-params(2))*T(1);
T(3) = 1/(1-params(4));
T(4) = (y(15)/y(13))^(-params(2))*T(3);
T(5) = T(1)*y(14)^(1-params(3))+T(3)*y(15)^(1-params(3));
T(6) = y(11)^params(6);
T(7) = params(3)/(params(3)-1);
T(8) = params(6)*y(9)/y(11);
T(9) = params(7)*y(10)/y(12);
T(10) = (y(5)*(1-params(8))/(y(14)*y(19)*params(6)))^(1/(params(6)-1));
T(11) = (y(6)/(y(15)*params(12)*params(7)))^(1/(params(7)-1));
lhs(1) = 1;
rhs(1) = params(1)*(1-params(5)+y(6)+y(4)*(y(5)-y(6)));
lhs(2) = y(2);
rhs(2) = y(3)-(1-params(5))*y(3);
lhs(3) = y(1);
rhs(3) = (y(3)*(y(6)+y(4)*(y(5)-y(6)))-y(2))/((1+params(10))*y(13));
lhs(4) = y(6);
rhs(4) = y(5);
lhs(5) = y(7);
rhs(5) = y(1)*T(2);
lhs(6) = y(8);
rhs(6) = y(1)*T(4);
lhs(7) = y(13);
rhs(7) = T(5)^(1/(1-params(3)));
lhs(8) = y(9);
rhs(8) = y(19)*T(6);
lhs(9) = y(10);
rhs(9) = params(12)*y(12)^params(7);
lhs(10) = y(16);
rhs(10) = y(14)*y(9)-y(11)*y(5)*(1-params(8));
lhs(11) = y(17);
rhs(11) = y(15)*y(10)-y(6)*y(12);
lhs(12) = y(14);
rhs(12) = T(7)*y(5)*(1-params(8))/T(8);
lhs(13) = y(15);
rhs(13) = T(7)*y(6)/T(9);
lhs(14) = y(11);
rhs(14) = T(10);
lhs(15) = y(12);
rhs(15) = T(11);
lhs(16) = y(3);
rhs(16) = T(10)/y(4);
lhs(17) = y(3);
rhs(17) = T(11)+y(4)*y(3);
lhs(18) = log(y(19));
rhs(18) = log(y(19))*params(9)+x(1);
lhs(19) = y(18);
rhs(19) = y(1)*params(10)*y(13)+params(11)*(y(16)+y(17))-y(11)*y(5)*params(8);
end
